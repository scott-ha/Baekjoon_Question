package Question;

import java.util.*;

public class Q_10998 {

	public static int solution(int a, int b) {
		return a * b;
	}
	
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a, b;
		
		a = sc.nextInt();
		b = sc.nextInt();
		
		System.out.println(solution(a,b));

	}

}
